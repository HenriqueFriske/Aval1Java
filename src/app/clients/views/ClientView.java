    private void btn_viewClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewClientActionPerformed

        try {
            int line = tb_clients.getSelectedRow();
            if (line < 0) {
                throw new IllegalArgumentException("Selecione uma registro para visualizar!");
            }

            String id = tb_clients.getValueAt(line, 0).toString();

            Client client = controller.getById(id);
            if (client == null) {
                throw new ClientValidationException("Client não encontrado");
            }

            tp_clientTabs.setSelectedIndex(1);

            txt_code.setText("" + client.getId());
            txt_name.setText(client.getName());
            txt_cpf.setText(client.getCpf());
            txt_email.setText(client.getEmail());
            txt_homePhone.setText(client.getHomePhone());
            txt_cellPhone.setText(client.getCellPhone());
            txt_address.setText(client.getAddress());
            txt_zipCode.setText(client.getZipCode());
            txt_number.setText("" + client.getNumber());
            txt_neighborhood.setText(client.getNeighborhood());
            txt_city.setText(client.getCity());
            cbx_uf.setSelectedItem(client.getUf());

            disableClientTextFields();

        } catch (DBException | ClientValidationException | IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }


    }//GEN-LAST:event_btn_viewClientActionPerformed

