    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        try {
            String code = txt_code.getText();
            String name = txt_name.getText();
            String cpf = txt_cpf.getText().replaceAll("[\\.\\-]", "");
            String email = txt_email.getText();
            String homePhone = txt_homePhone.getText().replaceAll("[\\s\\-()]", "");
            String cellPhone = txt_cellPhone.getText().replaceAll("[\\s\\-()]", "");
            String address = txt_address.getText();
            String zipCode = txt_zipCode.getText().replaceAll("[\\-]", "");
            String addressNumber = txt_number.getText();
            String neighborhood = txt_neighborhood.getText();
            String city = txt_city.getText();
            String uf = cbx_uf.getSelectedItem().toString();

            if (code.isEmpty()) {
                controller.createClient(name, cpf, email, homePhone, cellPhone, zipCode, address, addressNumber, neighborhood, city, uf);
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
            } else {
                controller.updateClient(Integer.parseInt(code), name, cpf, email, homePhone, cellPhone, zipCode, address, addressNumber, neighborhood, city, uf);
                JOptionPane.showMessageDialog(null, "Cadastro atualizado com sucesso!");
            }
            
            disableClientTextFields();
            loadClients();

        } catch (DBException | ClientValidationException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

