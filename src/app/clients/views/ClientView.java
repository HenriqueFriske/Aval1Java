    private void loadClients() {
        DefaultTableModel model = (DefaultTableModel) tb_clients.getModel();
        model.setRowCount(0);
        try {
            List<ClientSummaryDTO> clients = controller.getAllSummarizedClient();

            for (ClientSummaryDTO client : clients) {
                model.addRow(new Object[]{
                    client.id(), client.name(), client.cpf(), client.email(), client.homePhone(), client.cellPhone()
                });
            }
        } catch (DBException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

